    private fun filter(toString: String) {
        var filteredList: ArrayList<WordModel>
        filteredList = ArrayList()

        for (item in list) {
            if (item.wordEng.toLowerCase().contains(toString.toLowerCase())) {
                filteredList.add(item)
                filteredList.sortBy { wordModel -> wordModel.wordEng }
            }
        }

        adapter.filterList(filteredList)
    }

